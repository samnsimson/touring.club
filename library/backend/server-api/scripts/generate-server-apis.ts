import { readFileSync, writeFileSync } from 'fs';
import { join } from 'path';
import Handlebars from 'handlebars';
import prettier from 'prettier';
import { ApiSdkUtils, CLIENT_REGISTRY } from '@tc/api-sdk';

export async function generateServerApis() {
    console.log(`📝 [server-api] Generating ${CLIENT_REGISTRY.length} service API wrappers...`);

    const templatePath = join(__dirname, 'templates', 'server-api.template.hbs');
    const template = Handlebars.compile(readFileSync(templatePath, 'utf-8'));
    const apisDir = join(__dirname, '..', 'src', 'apis');
    const barrelExports: string[] = [];

    let index = 0;
    for (const service of CLIENT_REGISTRY) {
        index += 1;
        const pascalService = ApiSdkUtils.pascalCase(service);
        const camelService = ApiSdkUtils.camelCase(service);
        const content = template({ service, pascalService, camelService });
        const outputPath = join(apisDir, `${service}.server-api.ts`);

        const config = await prettier.resolveConfig(outputPath);
        const formatted = await prettier.format(content, { ...config, filepath: outputPath });
        writeFileSync(outputPath, formatted);
        barrelExports.push(`export { ${pascalService}Api } from './${service}.server-api';`);

        console.log(`  [${index}/${CLIENT_REGISTRY.length}] ✓ apis/${service}.server-api.ts`);
    }

    const barrelPath = join(apisDir, 'index.ts');
    const barrelContent = ['// This file is auto-generated by scripts/generate-server-apis.ts', ...barrelExports].join('\n');
    const barrelConfig = await prettier.resolveConfig(barrelPath);
    const formattedBarrel = await prettier.format(barrelContent, { ...barrelConfig, filepath: barrelPath });
    writeFileSync(barrelPath, formattedBarrel);
    console.log('  ✓ apis/index.ts (barrel)');

    console.log(`✅ [server-api] Generated ${CLIENT_REGISTRY.length} service API wrappers.`);
}

generateServerApis();
